ch = input("Enter the alphabet: ")
    
if ch>='A'and ch<='Z':
    {
        print("\nIt is a uppercase character")
    }
elif ch>='a' and ch<='z':
    {
        print("\nIt is a lowercase character")
    }
elif ch>='0' and ch<='9':
    {
        print("\nIt is a digit")
    }
else:
    {
        print("\nIt is a special character")
    }
